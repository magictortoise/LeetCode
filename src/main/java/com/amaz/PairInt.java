package com.amaz;

public class PairInt {
    private int relevance;
    private int price;

    public PairInt(int relevance, int price) {
        this.relevance = relevance;
        this.price = price;
    }

    public int getRelevance() {
        return relevance;
    }

    public int getPrice() {
        return price;
    }
}
